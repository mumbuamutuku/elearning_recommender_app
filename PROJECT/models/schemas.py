class Learner:
    def __init__(self, id, name, age, goals, preferences):
        self.id = id
        self.name = name
        self.age = age
        self.goals = goals
        self.preferences = preferences

class Course:
    def __init__(self, id, title, description, skills, difficulty, rating):
        self.id = id
        self.title = title
        self.description = description
        self.skills = skills
        self.difficulty = difficulty
        self.rating = rating
